export type Lang = keyof typeof ui;

export function getLang(url: URL): Lang {
  const path = url.pathname;
  if (path.startsWith('/ca')) return 'ca';
  if (path.startsWith('/en')) return 'en';
  return 'es';
}

export function t(lang: Lang, key: keyof typeof ui['es']): string {
  return (ui[lang] as Record<string, string>)[key] ?? (ui['es'] as Record<string, string>)[key] ?? key;
}

export function getRouteForLang(currentPath: string, targetLang: Lang): string {
  const base = currentPath.replace(/^\/(ca|en)/, '') || '/';
  if (targetLang === 'es') return base;
  return `/${targetLang}${base === '/' ? '' : base}`;
}
